# Exercise 4: Real-World Data Types

# Represent real-world information with appropriate data types
product_name = "Laptop Computer"    # What type should this be? - This is a (str)
price = 899.99                      # What type should this be?  - This is a (float)
quantity_in_stock = 15              # What type should this be?  - This is a (int)
is_on_sale = False                  # What type should this be?  - This is a (bool)

print("Product:", product_name)
print("Price: $", price)
print("Stock:", quantity_in_stock)
print("On Sale:", is_on_sale)

print("\nData Types:")
print("Product name type:", type(product_name))
print("Price type:", type(price))
print("Stock type:", type(quantity_in_stock))
print("On sale type:", type(is_on_sale))





