# Exercise 1: Data Type Explorer - Create variables of each type and examine them:

# Create one variable of each type
my_integer = 50
my_float = 50.5
my_bool = True         # Remember, Boolean represents True/False values, and the first letter is capitalized 
my_string = "Carter"
# Dont forget, strings are any data wrapped in quotes and are considered as text.
# "50" is not an integer data type, it's a string because of the number 50 is wrapped in quotes

# Print each variable and it's type
print("Integer value:", my_integer)
print("Integer value type is:", type(my_integer))
print()

print("Float value:", my_float)
print("Float value type is:", type(my_float))
print()

print("Boolean value:", my_bool)
print("Boolean value type is:", type(my_bool))
print()

print("String value:", my_string)
print("String vaue type is:", type(my_string))
print()
