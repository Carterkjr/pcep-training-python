# 2. Boolean capitalization:

# Correct:
is_ready = True
is_finished = False

# Wrong (these would be variable names, not boolean values):
# is_ready = true    		# NameError
# is_finished = false 		# NameError

# ******************************************************

# 3. Forgetting quotes for strings:

# Correct:
name = "John"

# name = John 		# NameError




